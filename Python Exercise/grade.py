getGrade = int(input("Enter Your grade"))

if getGrade >= 95:
    print("A")
elif getGrade >= 82:
    print("B")
elif getGrade >= 74:
    print("C")
elif getGrade >= 65:
    print("D")
elif getGrade >= 50:
    print("F") 





print(getGrade(95))  # Output: "A"
print(getGrade(82))  # Output: "B"
print(getGrade(74))  # Output: "C"
print(getGrade(65))  # Output: "D"
print(getGrade(50))  # Output: "F"
